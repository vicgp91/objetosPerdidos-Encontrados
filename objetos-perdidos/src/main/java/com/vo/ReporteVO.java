package com.vo;

public class ReporteVO {
private Integer reporteid;
private String reportetittle;
private String foto;
private String descripcion;
private String username;
private String estado;
private String celular;
private String correo;	
public Integer getReporteid() {
	return reporteid;
}
public void setReporteid(Integer reporteid) {
	this.reporteid = reporteid;
}
public String getReportetittle() {
	return reportetittle;
}
public void setReportetittle(String reportetittle) {
	this.reportetittle = reportetittle;
}
public String getFoto() {
	return foto;
}
public void setFoto(String foto) {
	this.foto = foto;
}
public String getDescripcion() {
	return descripcion;
}
public void setDescripcion(String descripcion) {
	this.descripcion = descripcion;
}
public String getUsername() {
	return username;
}
public void setUsername(String username) {
	this.username = username;
}
public String getEstado() {
	return estado;
}
public void setEstado(String estado) {
	this.estado = estado;
}
public String getCelular() {
	return celular;
}
public void setCelular(String celular) {
	this.celular = celular;
}
public String getCorreo() {
	return correo;
}
public void setCorreo(String correo) {
	this.correo = correo;
}

}
